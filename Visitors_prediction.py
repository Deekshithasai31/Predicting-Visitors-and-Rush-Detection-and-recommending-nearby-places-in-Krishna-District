# Function to calculate Haversine distance
def haversine_vectorized(lat1, lon1, lat2, lon2):
    R = 6371
    lat1_rad = np.radians(lat1)
    lon1_rad = np.radians(lon1)
    lat2_rad = np.radians(lat2)
    lon2_rad = np.radians(lon2)
    dlon = lon2_rad - lon1_rad
    dlat = lat2_rad - lat1_rad
    a = np.sin(dlat / 2)**2 + np.cos(lat1_rad) * np.cos(lat2_rad) * np.sin(dlon / 2)**2
    c = 2 * np.arcsin(np.sqrt(a))
    distance = R * c
    return distance
def recommend_nearby_places_improved(df, place_name, max_distance_km=100, top_k=5):
    if place_name not in df["name"].values:
        raise ValueError(f"Place '{place_name}' not found in dataset.")
    df = df.copy()
    df["lat"] = pd.to_numeric(df["lat"], errors="coerce")
    df["lon"] = pd.to_numeric(df["lon"], errors="coerce")
    if "date" in df.columns:
        idx = df.groupby("name")["date"].idxmax().dropna()
        place_latest = df.loc[idx].reset_index(drop=True)
    else:
        place_latest = df.groupby("name", as_index=False).last()

    place_latest = place_latest.dropna(subset=["lat", "lon"]).copy()
    main_row = place_latest[place_latest["name"] == place_name]
    if main_row.empty:
        raise ValueError(f"No coordinates found for '{place_name}'.")
    lat0 = float(main_row["lat"].iloc[0])
    lon0 = float(main_row["lon"].iloc[0])

    place_latest["distance_km"] = haversine_vectorized(
        lat0, lon0, place_latest["lat"].astype(float).values,place_latest["lon"].astype(float).values)

    candidates = place_latest[(place_latest["name"] != place_name)].copy()
    candidates = candidates.dropna(subset=["distance_km"])
    candidates = candidates.sort_values("distance_km")

    within_radius = candidates[candidates["distance_km"] <= max_distance_km]

    if len(within_radius) >= top_k:
        recs = within_radius.head(top_k).copy()
    else:
        recs = candidates.head(top_k).copy()

    recs = recs[["name", "lat", "lon", "distance_km", "category", "base_popularity"]].copy()
    recs["distance_km"] = recs["distance_km"].round(2)
    recs = recs.reset_index(drop=True)
    return recs

# Full Pipeline
def tourism_pipeline(df, place_name, forecast_days=7, top_k=5):
    forecast_df = forecast_visitors(df, place_name, periods=forecast_days)
    rush_status = rush_hybrid_score(df, place_name, forecast_df)
    recommendations = recommend_nearby_places_improved(df, place_name, max_distance_km=100, top_k=top_k)
    return forecast_df, rush_status, recommendations

# Evaluation Function
def evaluate_forecast_model(df, place_name, test_days=14):
    ts = get_place_timeseries(df, place_name)

    if len(ts) < test_days + 7:
        print(f"Not enough data for {place_name}. Need at least {test_days + 7} days.")
        return None

    train = ts[:-test_days].copy()
    test = ts[-test_days:].copy()

    model = Prophet(yearly_seasonality=True, weekly_seasonality=True)
    model.fit(train)

    future = model.make_future_dataframe(periods=test_days)
    forecast = model.predict(future)

    predictions = forecast.tail(test_days)["yhat"].values
    actuals = test["y"].values

    mae = mean_absolute_error(actuals, predictions)
    rmse = np.sqrt(mean_squared_error(actuals, predictions))
    mape = np.mean(np.abs((actuals - predictions) / (actuals + 1e-9))) * 100

    metrics = {
        "place": place_name,
        "MAE": round(mae, 2),
        "RMSE": round(rmse, 2),
        "MAPE (%)": round(mape, 2),
        "Test Days": test_days
    }

    return metrics, actuals, predictions, test["ds"].values

