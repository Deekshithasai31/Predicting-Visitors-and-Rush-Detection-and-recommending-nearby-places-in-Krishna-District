# Forecasting Functions
def get_place_timeseries(df, place_name):
    data = df[df["name"] == place_name][["date","visitors_count"]].copy()
    data = data.groupby("date")["visitors_count"].sum().reset_index()
    data.rename(columns={"date":"ds","visitors_count":"y"}, inplace=True)
    return data
def forecast_visitors(df, place_name, periods=7):
    ts = get_place_timeseries(df, place_name)
    model = Prophet(yearly_seasonality=True, weekly_seasonality=True)
    model.fit(ts)
    today = pd.to_datetime("today").normalize()
    future = pd.DataFrame({
        "ds": pd.date_range(
            start=today + pd.Timedelta(days=1),
            periods=periods,
            freq="D"
        )
    })
    forecast = model.predict(future)
    result = forecast[["ds", "yhat", "yhat_lower", "yhat_upper"]].copy()
    return result

# Rush Detection
def rush_hybrid_score(df, place_name, forecast_df, rel_weight=0.45, z_weight=0.35,pct_weight=0.2, rel_scale=0.3, z_scale=1.5, percentile=90):
    hist = df[df["name"] == place_name]["visitors_count"].dropna()
    if len(hist) == 0:
        return "🟩 No Rush – Not enough historical data"
    forecast_mean = float(forecast_df["yhat"].mean())
    hist_mean = hist.mean()
    hist_std = hist.std(ddof=0) if hist.std(ddof=0) > 0 else 1.0
    # Relative increase
    rel = max(0, (forecast_mean - hist_mean) / (hist_mean * rel_scale))
    rel = min(rel, 1.0)
    # Z-score deviation
    z = max(0, (forecast_mean - hist_mean) / (z_scale * hist_std))
    z = min(z, 1.0)
    # Percentile check
    pct_thresh = np.percentile(hist, percentile)
    pct = max(0, forecast_mean / pct_thresh)
    pct_norm = 0.5 * pct if pct <= 1 else 0.5 + min(1.0, (pct - 1.0))
    pct_norm = min(pct_norm, 1.0)
    score = rel_weight * rel + z_weight * z + pct_weight * pct_norm
    if score < 0.25:
        label = "🟩 No Rush – Normal visitor flow"
    elif score < 0.5:
        label = "🟨 Watch – Slightly higher visitors"
    elif score < 0.8:
        label = "🟥 Rush – High crowd expected"
    else:
        label = "🔴 Severe Rush – Heavy crowd likely"
    return label

